/**
 * Mock audit results for testing the results display
 * Based on WCAG 2.2 AA standards with categorization by principles
 */

export interface AuditIssue {
  id: string;
  principle: "perceivable" | "operable" | "understandable" | "robust";
  guideline: string;
  wcagLevel: "A" | "AA" | "AAA";
  severity: "critical" | "serious" | "moderate" | "minor";
  title: string;
  description: string;
  element: string;
  selector: string;
  impact: string;
  remediation: string;
  helpUrl: string;
  occurrences: number;
}

export interface AuditResult {
  url?: string;
  fileName?: string;
  documentType?: "html" | "pdf" | "docx";
  timestamp: string;
  summary: {
    totalIssues: number;
    critical: number;
    serious: number;
    moderate: number;
    minor: number;
    passed: number;
    failed: number;
  };
  issues: AuditIssue[];
}

